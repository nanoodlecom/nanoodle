#!/usr/bin/env node
// Bundle the sibling nanoodle-js package into play.html as a single classic
// <script id="njs-engine"> defining window.NanoodleEngine (replace-prep Phase E).
//
// Zero dependencies: a tiny ESM→registry transform tailored to nanoodle-js's
// house style — static named imports/exports between local ./*.mjs modules,
// one default export (mp4cat), dynamic import("node:…") left in place (it
// rejects at runtime in a browser, which is exactly the ffmpeg-fallback
// behavior we want). No namespace imports, no top-level await.
//
// Output is idempotent: the block sits between NJS-ENGINE markers in play.html
// and embeds a version stamp (sibling commit) plus a content hash that
// this script's own --check mode verifies.
//
// Usage: node scripts/gen-js-engine.mjs [--check]
//   --check  regenerate in memory and diff against the embedded block
//            (exit 1 on drift, exit 0 + skip message when no sibling repo)

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { createHash } from "node:crypto";
import { resolve, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const PLAY = join(HERE, "..", "play.html");
const VENDOR = join(HERE, "..", "vendor", "njs-engine.js");   // editor lazy-loads this (index.html stays lean)
const JS_ROOT = process.env.NANOODLE_JS ? resolve(process.env.NANOODLE_JS) : resolve(HERE, "../../nanoodle-js");
const SRC = join(JS_ROOT, "src");
const ENTRY = "browser.mjs";
const BEGIN = "<!-- NJS-ENGINE:BEGIN (generated by scripts/gen-js-engine.mjs — do not edit by hand) -->";
const END = "<!-- NJS-ENGINE:END -->";

const check = process.argv.includes("--check");

if (!existsSync(join(SRC, ENTRY))) {
  // No sibling checkout → can't regenerate, but --check can still prove the SHIPPED artifacts are
  // self-consistent: each embeds a content hash of its own payload, and both must carry the same
  // bundle. Catches a hand-edited/corrupted play block or vendor file that would otherwise ship
  // with every hook green.
  if (check) {
    const sha16 = (s) => createHash("sha256").update(s).digest("hex").slice(0, 16);
    const bad = [];
    const pm = /<script id="njs-engine" data-hash="([0-9a-f]{16})">\n([\s\S]*?)\n<\/script>/.exec(readFileSync(PLAY, "utf8"));
    if (pm && sha16(pm[2]) !== pm[1]) bad.push("play.html njs-engine block content does not match its data-hash");
    let vm = null;
    if (existsSync(VENDOR)) {
      vm = /^\/\* data-hash=([0-9a-f]{16}) \*\/\n([\s\S]*)\n$/.exec(readFileSync(VENDOR, "utf8"));
      if (!vm) bad.push("vendor/njs-engine.js has no data-hash header");
      else if (sha16(vm[2]) !== vm[1]) bad.push("vendor/njs-engine.js content does not match its data-hash");
    }
    if (pm && vm && pm[1] !== vm[1]) bad.push("play.html block and vendor/njs-engine.js carry different bundles");
    if (bad.length) { console.error(`✗ njs-engine (no sibling checkout): ${bad.join("; ")} — restore from git or regenerate with nanoodle-js checked out`); process.exit(1); }
    console.log(`⊘ skip njs-engine check: nanoodle-js not found at ${JS_ROOT} (shipped artifacts are self-consistent)`);
    process.exit(0);
  }
  console.log(`⊘ skip njs-engine gen: nanoodle-js not found at ${JS_ROOT}`);
  process.exit(0);
}

/* ---- ESM → registry transform ------------------------------------------- */

function stripLocalImports(code, deps) {
  // import { a, b as c } from "./x.mjs";  (possibly multiline)
  return code.replace(
    /^import\s*\{([^}]*)\}\s*from\s*["']\.\/([\w.-]+)["'];?/gm,
    (_, names, mod) => {
      deps.add(mod);
      const bind = names.replace(/\s+as\s+/g, ": ").replace(/\s+/g, " ").trim().replace(/,\s*$/, "");
      return `const { ${bind} } = __req("${mod}");`;
    });
}

function transformModule(name, code) {
  const deps = new Set();
  const exportNames = [];
  let out = stripLocalImports(code, deps);

  if (/^import\s/m.test(out.replace(/^\s*\/\/.*$/gm, "")) && /from\s*["']\.\//m.test(out)) {
    throw new Error(`${name}: unhandled import form — extend gen-js-engine.mjs`);
  }

  // export { a, b as c } from "./x.mjs";
  out = out.replace(
    /^export\s*\{([^}]*)\}\s*from\s*["']\.\/([\w.-]+)["'];?/gm,
    (_, names, mod) => {
      deps.add(mod);
      const assigns = names.split(",").map((s) => s.trim()).filter(Boolean).map((s) => {
        const m = /^([\w$]+)(?:\s+as\s+([\w$]+))?$/.exec(s);
        if (!m) throw new Error(`${name}: unparseable re-export "${s}"`);
        return `__x.${m[2] || m[1]} = __m.${m[1]};`;
      }).join(" ");
      return `{ const __m = __req("${mod}"); ${assigns} }`;
    });

  // export { a, b as c };  (bare list)
  out = out.replace(/^export\s*\{([^}]*)\};?[ \t]*$/gm, (_, names) => {
    return names.split(",").map((s) => s.trim()).filter(Boolean).map((s) => {
      const m = /^([\w$]+)(?:\s+as\s+([\w$]+))?$/.exec(s);
      if (!m) throw new Error(`${name}: unparseable export "${s}"`);
      return `__x.${m[2] || m[1]} = ${m[1]};`;
    }).join(" ");
  });

  // export default X;
  out = out.replace(/^export\s+default\s+([\w$]+);?\s*$/gm, (_, id) => `__x.default = ${id};`);

  // export (async) function / class / const / let / var
  out = out.replace(/^export\s+(async\s+function|function|class|const|let|var)\s+([\w$]+)/gm, (_, kind, id) => {
    exportNames.push(id);
    return `${kind} ${id}`;
  });

  if (/^export\s/m.test(out)) {
    const line = out.split("\n").find((l) => /^export\s/.test(l));
    throw new Error(`${name}: unhandled export form: ${line}`);
  }

  const tail = exportNames.length ? `\n${exportNames.map((id) => `__x.${id} = ${id};`).join(" ")}` : "";
  return { deps: [...deps], body: out + tail };
}

/* ---- build ---------------------------------------------------------------- */

const modules = new Map(); // name → { deps, body }
const queue = [ENTRY];
while (queue.length) {
  const name = queue.shift();
  if (modules.has(name)) continue;
  const file = join(SRC, name);
  if (!existsSync(file)) throw new Error(`module not found: ${name}`);
  const t = transformModule(name, readFileSync(file, "utf8"));
  modules.set(name, t);
  queue.push(...t.deps);
}

// Version = hash of the bundled SOURCES, not the sibling's git HEAD — a docs-only
// commit over there must not flag the embedded bundle as stale.
const srcHash = createHash("sha256");
for (const [name] of [...modules.entries()].sort()) srcHash.update(name).update(readFileSync(join(SRC, name)));
const version = "src-" + srcHash.digest("hex").slice(0, 12);

const defs = [...modules.entries()].map(([name, m]) =>
  `__def(${JSON.stringify(name)}, function (__x, __req) {\n${m.body}\n});`).join("\n");

let bundle = `/* nanoodle-js browser engine — generated from nanoodle-js@${version} (${modules.size} modules) */
(function () {
  "use strict";
  var __mods = {};
  function __def(name, fn) { __mods[name] = { fn: fn, x: null }; }
  function __req(name) {
    var m = __mods[name];
    if (!m) throw new Error("njs-engine: missing module " + name);
    if (!m.x) { m.x = {}; m.fn(m.x, __req); }
    return m.x;
  }
${defs}
  window.NanoodleEngine = __req(${JSON.stringify(ENTRY)});
  window.NanoodleEngine.version = ${JSON.stringify(version)};
})();`;

// a literal </script> anywhere in the bundle would end the tag — escape is byte-equivalent in JS strings
bundle = bundle.replace(/<\/script/gi, "<\\/script");

const hash = createHash("sha256").update(bundle).digest("hex").slice(0, 16);
const block = `${BEGIN}\n<script id="njs-engine" data-hash="${hash}">\n${bundle}\n</script>\n${END}`;

/* ---- syntax-check the transformed bundle before touching play.html -------- */
new Function("window", bundle); // throws on any transform-induced syntax error (window shadowed, nothing executed)

/* ---- splice into play.html ------------------------------------------------ */

const html = readFileSync(PLAY, "utf8");
const re = new RegExp(BEGIN.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") + "[\\s\\S]*?" + END.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"));

let next;
if (re.test(html)) {
  next = html.replace(re, block);
} else {
  // first insertion: a classic script parsed before the (deferred) builder module runs
  const anchor = '<script type="module">';
  const at = html.indexOf(anchor);
  if (at === -1) throw new Error("play.html: builder <script type=\"module\"> not found");
  next = html.slice(0, at) + block + "\n" + html.slice(at);
}

// Same bundle as a standalone file for the EDITOR (index.html lazy-loads it only when the
// njs_engine flag is on, so the landing page never carries the bundle's weight). Byte-identical
// payload to the play block — one generator, one source of truth. data-hash rides in a comment.
const vendorFile = `/* data-hash=${hash} */\n${bundle}\n`;
const vendorCur = existsSync(VENDOR) ? readFileSync(VENDOR, "utf8") : null;

if (check) {
  const stale = [];
  if (next !== html) stale.push("play.html");
  if (vendorCur !== vendorFile) stale.push("vendor/njs-engine.js");
  if (!stale.length) { console.log(`✓ njs-engine: embedded bundle is current (nanoodle-js@${version}, ${modules.size} modules, ${(bundle.length / 1024).toFixed(0)} KB)`); process.exit(0); }
  console.error(`✗ njs-engine: ${stale.join(" + ")} stale — run: node scripts/gen-js-engine.mjs`);
  process.exit(1);
}

if (next === html && vendorCur === vendorFile) {
  console.log(`✓ njs-engine: already current (nanoodle-js@${version})`);
} else {
  if (next !== html) writeFileSync(PLAY, next);
  if (vendorCur !== vendorFile) writeFileSync(VENDOR, vendorFile);
  console.log(`✓ njs-engine: embedded nanoodle-js@${version} (${modules.size} modules, ${(bundle.length / 1024).toFixed(0)} KB) into play.html + vendor/njs-engine.js`);
}
