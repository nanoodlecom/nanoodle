#!/usr/bin/env node
// Rebuild runware-airs.json (and stamp the inlined RUNWARE-AIRS block in index.html)
// from NanoGPT's public image catalog + a small seed of verified platform AIRs.
//
//   node scripts/update-runware-airs.mjs
//   node scripts/update-runware-airs.mjs --probe   # optional live check (needs NANOGPT_API_KEY; ~$0.005/OK)
//
// The editor never fetches this at runtime — the stamped const is the source of truth
// for the custom-civitai dropdowns. Re-run when NanoGPT adds more runware:/persona: models.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const JSON_OUT = join(root, "runware-airs.json");
const INDEX = join(root, "index.html");
const CATALOG_URL = "https://nano-gpt.com/api/v1/image-models";
const GEN_URL = "https://nano-gpt.com/v1/images/generations";
const probe = process.argv.includes("--probe");
const key = process.env.NANOGPT_API_KEY || "";

// Extra platform AIRs that resolve on custom-civitai but may not appear as standalone
// catalog rows. Names are human labels only; the air is what the API receives.
const SEED = [
  { air: "runware:100@1", name: "Flux (runware:100@1)", group: "runware" },
  { air: "runware:400@1", name: "FLUX.2 [dev] (runware:400@1)", group: "runware" },
  { air: "runware:106@1", name: "Flux Kontext Dev", group: "runware" },
  { air: "runware:107@1", name: "Flux 1 Krea Dev", group: "runware" },
  { air: "persona:376130@2456367", name: "Nova Anime XL", group: "persona" },
];

const GROUP_META = {
  runware: { id: "runware", label: "Runware", order: 0 },
  persona: { id: "persona", label: "Curated", order: 1 },
};

function groupOf(air) {
  if (air.startsWith("runware:")) return "runware";
  if (air.startsWith("persona:")) return "persona";
  return null;
}

async function fetchCatalog() {
  const r = await fetch(CATALOG_URL, { headers: { Accept: "application/json" } });
  if (!r.ok) throw new Error(`catalog HTTP ${r.status}`);
  const j = await r.json();
  return j.data || j || [];
}

async function probeAir(air) {
  if (!key) throw new Error("--probe requires NANOGPT_API_KEY");
  const body = {
    model: "custom-civitai",
    prompt: "a simple red apple on a table",
    size: "512x512",
    n: 1,
    response_format: "b64_json",
    customCivitaiAir: air,
  };
  const r = await fetch(GEN_URL, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${key}`,
      "x-api-key": key,
    },
    body: JSON.stringify(body),
  });
  if (r.ok) return { ok: true };
  let msg = "";
  try {
    const j = await r.json();
    msg = j?.error?.code || j?.error?.message || "";
  } catch {
    msg = await r.text();
  }
  return { ok: false, status: r.status, msg: String(msg).slice(0, 120) };
}

function stampIndex(payload) {
  const src = readFileSync(INDEX, "utf8");
  const begin = "/* === RUNWARE-AIRS-BEGIN === */";
  const end = "/* === RUNWARE-AIRS-END === */";
  const bi = src.indexOf(begin);
  const ei = src.indexOf(end);
  if (bi < 0 || ei < 0 || ei < bi) {
    throw new Error("RUNWARE-AIRS markers not found in index.html — add the block first");
  }
  const block =
    begin +
    "\n// Generated by scripts/update-runware-airs.mjs — do not hand-edit.\n" +
    "const RUNWARE_AIRS = " +
    JSON.stringify(payload, null, 2) +
    ";\n" +
    end;
  const next = src.slice(0, bi) + block + src.slice(ei + end.length);
  writeFileSync(INDEX, next);
}

async function main() {
  const byAir = new Map();
  for (const s of SEED) {
    byAir.set(s.air, { air: s.air, name: s.name, group: s.group, source: "seed" });
  }

  let catalogCount = 0;
  try {
    const items = await fetchCatalog();
    for (const m of items) {
      const air = String(m.id || "");
      const g = groupOf(air);
      if (!g) continue;
      catalogCount++;
      const prev = byAir.get(air);
      byAir.set(air, {
        air,
        name: m.name || (prev && prev.name) || air,
        group: g,
        source: "nanogpt-catalog",
      });
    }
    console.log(`catalog: ${catalogCount} runware:/persona: model(s)`);
  } catch (e) {
    console.warn("catalog fetch failed — keeping seed only:", e.message);
  }

  let models = [...byAir.values()].sort((a, b) =>
    a.group === b.group ? a.name.localeCompare(b.name) : (GROUP_META[a.group]?.order ?? 9) - (GROUP_META[b.group]?.order ?? 9)
  );

  if (probe) {
    console.log(`probing ${models.length} AIR(s) via custom-civitai…`);
    const kept = [];
    for (const m of models) {
      process.stdout.write(`  ${m.air} … `);
      try {
        const r = await probeAir(m.air);
        if (r.ok) {
          console.log("OK");
          kept.push(m);
        } else {
          console.log(`FAIL ${r.status || ""} ${r.msg || ""}`.trim());
        }
      } catch (e) {
        console.log("ERR", e.message);
      }
    }
    models = kept;
  }

  const groupsMap = new Map();
  for (const m of models) {
    const meta = GROUP_META[m.group] || { id: m.group, label: m.group, order: 50 };
    if (!groupsMap.has(meta.id)) groupsMap.set(meta.id, { id: meta.id, label: meta.label, order: meta.order, models: [] });
    groupsMap.get(meta.id).models.push({ air: m.air, name: m.name, source: m.source });
  }
  const groups = [...groupsMap.values()]
    .sort((a, b) => a.order - b.order)
    .map(({ id, label, models: ms }) => ({ id, label, models: ms }));

  const payload = {
    generated: new Date().toISOString().slice(0, 10),
    groups,
  };

  writeFileSync(JSON_OUT, JSON.stringify(payload, null, 2) + "\n");
  stampIndex(payload);
  const n = groups.reduce((s, g) => s + g.models.length, 0);
  console.log(`wrote ${JSON_OUT} (${n} models in ${groups.length} group(s))`);
  console.log(`stamped RUNWARE_AIRS in index.html`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
